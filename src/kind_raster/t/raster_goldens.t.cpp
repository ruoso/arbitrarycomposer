DecodedImage input_image() {
  DecodedImage img;
  img.width = 4;
  img.height = 4;
  img.format = k_working_rgba32f;
  img.bytes.resize(kInput.size() * sizeof(float));
  std::memcpy(img.bytes.data(), kInput.data(), kInput.size() * sizeof(float));
  return img;
}

std::vector<std::byte> bytes_of_floats(const std::vector<float>& f) {
  std::vector<std::byte> out(f.size() * sizeof(float));
  std::memcpy(out.data(), f.data(), out.size());
  return out;
}

std::vector<std::byte> bytes_of(const Surface& s) {
  const std::span<const std::byte> b = s.cpu_bytes();
  return {b.begin(), b.end()};
}

void require_bytes(const std::vector<std::byte>& got, std::span<const unsigned char> want) {
  REQUIRE(got.size() == want.size());
  REQUIRE(std::memcmp(got.data(), want.data(), want.size()) == 0);
}

// --- golden builders (the real shipped render path) -------------------------

std::vector<std::byte> mip_level_bytes(std::size_t level) {
  RasterContent content(input_image(), k_edge);
  const TileTablePtr table = content.store().base_table();
  return bytes_of_floats(table->level_pixels(level));
}

// Render `region` at `scale`/`exactness` into a `dim x dim` target of `fmt`.
std::vector<std::byte> render_bytes(int dim, double scale, Exactness ex, SurfaceFormat fmt,
                                    RenderResult& meta_out) {
  RasterContent content(input_image(), k_edge);
  MemSurface target(dim, dim, fmt);
  RenderRequest req{Rect{0.0, 0.0, 4.0, 4.0},
                    scale,
                    Time::zero(),
                    StateHandle{},
                    target,
                    ex,
                    ex == Exactness::Exact ? Deadline::none() : Deadline{}};
  auto done = std::make_shared<RenderCompletion>();
  const std::optional<RenderResult> r = content.render(req, done);
  REQUIRE(r.has_value());
  meta_out = *r;
  return bytes_of(target);
}

