// Byte-exact reference-vector golden suite for org.arbc.raster (doc 16 tier-3
// "Golden rendering tests"; mirrors the color.kernel_goldens pattern). For a
// fixed decoded buffer it pins, with NO tolerance:
//   - each mip level's bytes (Lanczos-3 half-band decimation determinism);
//   - render at native / 0.5x / 0.25x (mip rungs) and past native (BestEffort
//     clamp), plus the exact achieved_scale / exact values;
//   - render into each of the three surface formats, proving format-generic
//     read/write without a backend-cpu kernel.
//
// The 4x4 fixture is almost all edge-clamp and samples only integer phases, so it
// cannot exercise a 6-tap kernel's interior or a fractional-phase cubic tap at all.
// Two further fixtures below do: a 16x16 high-frequency pattern at k_edge = 8 (so
// level 0 is 2x2 tiles and the decimator reads ACROSS a tile boundary), and an
// 8x8 hard alpha step that forces the negative lobes to ring.
//
// enforces: 16-sdlc-and-quality#byte-exact-goldens
//
// REGENERATE PROCEDURE (doc 16 tier-3): an intended rendering change deliberately
// re-freezes these tables; they never regenerate silently. Build the target and
// run only the hidden dump case, which prints paste-ready literals:
//
//     cmake --build --preset dev --target arbc_kind_raster_t
//     ./build/dev/src/kind_raster/arbc_kind_raster_t "[.regen]"
//
// then replace the "FROZEN EXPECTED TABLES" block with its output.

