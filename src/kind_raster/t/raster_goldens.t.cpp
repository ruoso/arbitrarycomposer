// A CPU-backed surface of an arbitrary format for byte-exact golden compares.
class MemSurface final : public Surface {
public:
  MemSurface(int w, int h, SurfaceFormat fmt)
      : d_w(w), d_h(h), d_fmt(fmt),
        d_bytes(static_cast<std::size_t>(w) * static_cast<std::size_t>(h) *
                    bytes_per_pixel(fmt.pixel_format),
                std::byte{0}) {}

  int width() const override { return d_w; }
  int height() const override { return d_h; }
  SurfaceFormat format() const override { return d_fmt; }
  std::span<std::byte> cpu_bytes() override { return d_bytes; }
  std::span<const std::byte> cpu_bytes() const override { return d_bytes; }

private:
  int d_w;
  int d_h;
  SurfaceFormat d_fmt;
  std::vector<std::byte> d_bytes;
};

