// A 16x16 synthetic pattern: a 1-pixel checkerboard -- the high-frequency component
// a box filter aliases into mush and Lanczos-3 low-passes away -- riding a ramp.
// Every constant is an exactly representable binary fraction, so the fixture itself
// is byte-stable.
WorkingPixel hf_pixel(int x, int y) {
  const float chk = ((x + y) % 2 == 0) ? 0.875F : 0.125F;
  const float ramp = static_cast<float>(x) * 0.03125F + static_cast<float>(y) * 0.015625F;
  return {chk, ramp, chk * 0.5F, 1.0F};
}

constexpr int k_step_dim = 8;

// An 8x8 hard alpha step: transparent black left half, opaque white right half.
// Both kernels UNDERSHOOT at this edge, so it is the fixture that forces the
// non-negative clamp to matter -- an unclamped negative alpha would break
// unpremultiplication in PixelTraits<Rgba8Srgb>::encode.
WorkingPixel step_pixel(int x, int) {
  return x < k_step_dim / 2 ? WorkingPixel{0.0F, 0.0F, 0.0F, 0.0F}
                            : WorkingPixel{1.0F, 1.0F, 1.0F, 1.0F};
}

DecodedImage image_of(int dim, WorkingPixel (*gen)(int, int)) {
  DecodedImage img;
  img.width = dim;
  img.height = dim;
  img.format = k_working_rgba32f;
  std::vector<float> f;
  for (int y = 0; y < dim; ++y) {
    for (int x = 0; x < dim; ++x) {
      const WorkingPixel p = gen(x, y);
      f.insert(f.end(), p.begin(), p.end());
    }
  }
  img.bytes.resize(f.size() * sizeof(float));
  std::memcpy(img.bytes.data(), f.data(), img.bytes.size());
  return img;
}

std::vector<std::byte> hf_mip_bytes(std::size_t level) {
  RasterContent content(image_of(k_hf_dim, hf_pixel), k_hf_edge);
  return bytes_of_floats(content.store().base_table()->level_pixels(level));
}

std::vector<std::byte> step_mip_bytes(std::size_t level) {
  RasterContent content(image_of(k_step_dim, step_pixel), k_step_dim);
  return bytes_of_floats(content.store().base_table()->level_pixels(level));
}

// Render an arbitrary fixture over an arbitrary region -- the 4x4 suite's
// `render_bytes` is pinned to its own fixture and to region [0,4)x[0,4).
std::vector<std::byte> render_of(const DecodedImage& img, int edge, const Rect& region, int dim,
                                 double scale, Exactness ex, RenderResult& meta_out) {
  RasterContent content(img, edge);
  MemSurface target(dim, dim, k_working_rgba32f);
  RenderRequest req{region,
                    scale,
                    Time::zero(),
                    StateHandle{},
                    target,
                    ex,
                    ex == Exactness::Exact ? Deadline::none() : Deadline{}};
  auto done = std::make_shared<RenderCompletion>();
  const std::optional<RenderResult> r = content.render(req, done);
  REQUIRE(r.has_value());
  meta_out = *r;
  return bytes_of(target);
}

// The magnifier at phases no pre-existing golden reaches. At scale 2.0 the
// destination centres land on 0.25/0.75; at 0.75 they land on an irrational-ish
// ladder of phases. These are the ONLY cases that pin the cubic tap at all -- every
// case above samples at integer phase, where it collapses to the source sample.
std::vector<std::byte> mag_2x_bytes(RenderResult& m) {
  return render_of(input_image(), k_edge, Rect{0.0, 0.0, 4.0, 4.0}, 8, 2.0, Exactness::Exact, m);
}
std::vector<std::byte> mag_075_bytes(RenderResult& m) {
  return render_of(input_image(), k_edge, Rect{0.0, 0.0, 4.0, 4.0}, 3, 0.75, Exactness::Exact, m);
}
// A fractional-phase magnification straddling the hard alpha step: the magnifier's
// ringing case.
std::vector<std::byte> step_mag_bytes(RenderResult& m) {
  return render_of(image_of(k_step_dim, step_pixel), k_step_dim, Rect{3.0, 3.0, 5.0, 5.0}, 4, 2.0,
                   Exactness::Exact, m);
}

// The level-1 rung a 2x2 BOX average would have produced from the same fixture --
// the baseline this task replaces. A golden alone cannot distinguish "Lanczos-3
// landed" from "someone re-froze the box output"; this reference can.
std::vector<float> hf_box_level1() {
  std::vector<float> out;
  for (int y = 0; y < k_hf_dim / 2; ++y) {
    for (int x = 0; x < k_hf_dim / 2; ++x) {
      const WorkingPixel a = hf_pixel(2 * x, 2 * y);
      const WorkingPixel b = hf_pixel(2 * x + 1, 2 * y);
      const WorkingPixel c = hf_pixel(2 * x, 2 * y + 1);
      const WorkingPixel d = hf_pixel(2 * x + 1, 2 * y + 1);
      for (std::size_t k = 0; k < 4; ++k) {
        out.push_back((a[k] + b[k] + c[k] + d[k]) * 0.25F);
      }
    }
  }
  return out;
}

